# int(var_name)  ,  str(var_name)  ,  float(var_name)

a = "23";
b = int(a);
print(a, type(a), b, type(b), sep = " , ");

c = 49;
d = str(c);
print(c, type(c), d, type(d), sep = " , ");

e = 5
f = float(e);
print(e, type(e), f, type(f), sep = " , ");

